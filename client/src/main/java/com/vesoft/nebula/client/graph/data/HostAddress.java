package com.vesoft.nebula.client.graph.data;

public class HostAddress {
    private String host;
    private int port;

    public HostAddress(String host, int port) {
        this.host = host;
        this.port = port;
    }

    public String getHost() {
        return host;
    }

    public int getPort() {
        return port;
    }

    @Override
    public String toString() {
        return host +  ":" + port;
    }
}
